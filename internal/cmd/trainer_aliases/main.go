// trainer_aliases prints out and writes the ModelFnCompatible constraints and createModelFnWrapper function.
// It is an internal tool, meant to be used by developers of GoMLX.
package main

import (
	"fmt"
	"os"
	"os/exec"
	"path"
	"strings"

	"github.com/gomlx/gomlx/internal/must"
)

const outputFile = "gen_traineraliases.go"

type inputConfig struct {
	sig      string
	callArgs string
}

type outputConfig struct {
	name        string
	sig         string
	wrapOutputs func(call string) string
}

func main() {
	outputPath := path.Join(must.M1(os.Getwd()), outputFile)
	f := must.M1(os.Create(outputPath))

	fmt.Fprintln(f, "// ***** This file is generated by internal/cmd/trainer_aliases. DO NOT EDIT DIRECTLY. *****")
	fmt.Fprintln(f)
	fmt.Fprintln(f, "package train")
	fmt.Fprintln(f)
	fmt.Fprintln(f, "import (")
	fmt.Fprintln(f, "\t\"fmt\"")
	fmt.Fprintln(f, "\t\"github.com/gomlx/gomlx/core/graph\"")
	fmt.Fprintln(f, "\t\"github.com/gomlx/gomlx/ml/model\"")
	fmt.Fprintln(f, ")")
	fmt.Fprintln(f)

	var inputConfigs []inputConfig
	for _, hasSpec := range []bool{false, true} {
		for _, numNodes := range []int{0, 1, 2, 3, -1} {
			var sigParts []string
			sigParts = append(sigParts, "*model.Scope")
			if hasSpec {
				sigParts = append(sigParts, "any")
			}
			var callParts []string
			callParts = append(callParts, "scope")
			if hasSpec {
				callParts = append(callParts, "spec")
			}
			if numNodes == -1 {
				sigParts = append(sigParts, "[]*graph.Node")
				callParts = append(callParts, "inputs")
			} else {
				for i := 0; i < numNodes; i++ {
					sigParts = append(sigParts, "*graph.Node")
					callParts = append(callParts, fmt.Sprintf("inputs[%d]", i))
				}
			}
			inputConfigs = append(inputConfigs, inputConfig{
				sig:      strings.Join(sigParts, ", "),
				callArgs: strings.Join(callParts, ", "),
			})
		}
	}

	possibleOutputs := []outputConfig{
		{"ZeroOutputs", "", func(call string) string {
			return fmt.Sprintf("%s; return nil", call)
		}},
		{"OneOutput", "*graph.Node", func(call string) string {
			return fmt.Sprintf("return []*graph.Node{%s}", call)
		}},
		{"TwoOutputs", "(*graph.Node, *graph.Node)", func(call string) string {
			return fmt.Sprintf("r0, r1 := %s; return []*graph.Node{r0, r1}", call)
		}},
		{"ThreeOutputs", "(*graph.Node, *graph.Node, *graph.Node)", func(call string) string {
			return fmt.Sprintf("r0, r1, r2 := %s; return []*graph.Node{r0, r1, r2}", call)
		}},
		{"SliceOutputs", "[]*graph.Node", func(call string) string {
			return fmt.Sprintf("return %s", call)
		}},
	}

	var interfaceNames []string
	for _, out := range possibleOutputs {
		interfaceName := fmt.Sprintf("ModelFnCompatible%s", out.name)
		interfaceNames = append(interfaceNames, interfaceName)
		fmt.Fprintf(f, "type %s interface {\n", interfaceName)
		var parts []string
		sigOut := out.sig
		if sigOut != "" {
			sigOut = " " + sigOut
		}
		for _, in := range inputConfigs {
			parts = append(parts, fmt.Sprintf("\t~func(%s)%s", in.sig, sigOut))
		}
		fmt.Fprintf(f, "%s\n}\n\n", strings.Join(parts, " |\n"))
	}

	fmt.Fprintln(f, "// ModelFnCompatible represents a set of function signatures that are compatible aliases")
	fmt.Fprintln(f, "// to the canonical [ModelFn].")
	fmt.Fprintln(f, "//")
	fmt.Fprintln(f, "// NewTrainer accepts any function matching this constraint and automatically wraps it")
	fmt.Fprintln(f, "// to conform to the ModelFn signature.")
	fmt.Fprintln(f, "//")
	fmt.Fprintln(f, "// Notice that the optional 'any' parameter in these signatures is always the opaque 'spec'")
	fmt.Fprintln(f, "// parameter provided by datasets (usually only used for datasets with heterogeneous input")
	fmt.Fprintln(f, "// data for multi-task training).")
	fmt.Fprintln(f, "type ModelFnCompatible interface {")
	fmt.Fprintf(f, "\t%s\n", strings.Join(interfaceNames, " |\n\t"))
	fmt.Fprintln(f, "}")
	fmt.Fprintln(f)

	fmt.Fprintln(f, "func createModelFnWrapper[ModelFnT ModelFnCompatible](modelFn ModelFnT) ModelFn {")
	fmt.Fprintln(f, "\tswitch f := any(modelFn).(type) {")
	fmt.Fprintln(f, "\tcase ModelFn:")
	fmt.Fprintln(f, "\t\treturn f")
	for _, out := range possibleOutputs {
		sigOut := out.sig
		if sigOut != "" {
			sigOut = " " + sigOut
		}
		for _, in := range inputConfigs {
			fmt.Fprintf(f, "\tcase func(%s)%s:\n", in.sig, sigOut)
			if in.sig == "*model.Scope, any, []*graph.Node" && sigOut == " []*graph.Node" {
				fmt.Fprintf(f, "\t\treturn f\n")
			} else {
				fmt.Fprintf(f, "\t\treturn func(scope *model.Scope, spec any, inputs []*graph.Node) []*graph.Node {\n")
				fmt.Fprintf(f, "\t\t\t%s\n", out.wrapOutputs(fmt.Sprintf("f(%s)", in.callArgs)))
				fmt.Fprintf(f, "\t\t}\n")
			}
		}
	}
	fmt.Fprintln(f, "\tdefault:")
	fmt.Fprintf(f, "\t\tpanic(fmt.Sprintf(\"invalid modelFn type %%T\", modelFn))\n")
	fmt.Fprintln(f, "\t}")
	fmt.Fprintln(f)
	fmt.Fprintln(f, "}")

	must.M(f.Close())
	cmd := exec.Command("go", "fmt", outputPath)
	cmd.Stderr = os.Stderr
	must.M(cmd.Run())

	fmt.Printf("✅ successfully generated %s\n", outputPath)
}
